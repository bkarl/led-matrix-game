uint8_t flag_rotary;

#define COLORDUINO_ADDRESS 5
#define START_OF_DATA 0x10       //data markers
#define END_OF_DATA 0x20         //data markers

int main(void)
{
  HAL_Init();
  SystemClock_Config();
  flag_debounce = 0;

  __HAL_RCC_GPIOB_CLK_ENABLE();
  GPIO_InitTypeDef GPIO_InitStruct = {0};
  GPIO_InitStruct.Pin = GPIO_PIN_3;
  GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
  GPIO_InitStruct.Pull = GPIO_NOPULL;
  GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
  HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);
  setup_encoder();
  TIM6_Init();
  setup_i2c();
  uint8_t data[67];
  data[0] = START_OF_DATA;
  data[1] = 2;

  for (int i = 2; i < sizeof(data); i++) {
    data[i] = i * 2;
  }
  data[66] = END_OF_DATA;
  
  uint8_t cmd_word[3*67] = {0};
  UserInput ui = {0};
  user_input(ui);
  get_matrix_cmd_word(cmd_word);
  for (int i = 0; i < 3; i++)
    HAL_I2C_Master_Transmit(&hi2c1, COLORDUINO_ADDRESS * 2, cmd_word + i*sizeof(data), sizeof(data), 1000);
  
  uint32_t val = 0;
  while(1) 
  {
    val = TIM1->CNT;
    //HAL_Delay(1000);
    if (flag_rotary && !flag_debounce)
    {
      flag_debounce = 1;
      flag_rotary = 0;
      HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_3);
    }
  }
  return 0;
}

